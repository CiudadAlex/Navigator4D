package org.leviatanplatform.navigator4d.graphic;

/**
 * Pixel.
 * 
 * @author Alejandro
 * 
 */
public class Pixel {

    private final float x;
    private final float y;

    public Pixel(final float x, final float y) {

        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

}
